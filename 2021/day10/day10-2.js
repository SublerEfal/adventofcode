function getAutocompletePoints(line){
    let queue = [];
    for(let i=0; i<line.length; i++){
        let char = line.charAt(i);
        if(isOpener(char)){
            queue.push(getCloser(char));
        }else{
            if(queue.pop() !== char){
                return -1;
            }
        }
    }
    return calculateAutocompleteScore(queue);
}

function calculateAutocompleteScore(queue){
    let score = 0;
    for(let i=queue.length-1; i>=0; i--){
        score *= 5;
        score += getCloserPoints(queue[i]);
    }
    return score;
}

function isOpener(char){
    return /[\[\{\<\(]/.test(char);
}

function getCloser(char){
    switch(char){
        case "(": return ")";
        case "[": return "]";
        case "{": return "}";
        case "<": return ">";
    };
}

function getCloserPoints(char){
    switch(char){
        case ")": return 1;
        case "]": return 2;
        case "}": return 3;
        case ">": return 4;
    };
}

